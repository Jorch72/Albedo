package elucent.albedo.lighting;

public class Light {
	public float x;
	public float y;
	public float z;
	public float r;
	public float g;
	public float b;
	public float a;
	public float radius;
	
	public Light(float x, float y, float z, float r, float g, float b, float a, float radius){
		this.x = x;
		this.y = y;
		this.z = z;
		this.r = r;
		this.g = g;
		this.b = b;
		this.a = a;
		this.radius = radius;
	}
}
